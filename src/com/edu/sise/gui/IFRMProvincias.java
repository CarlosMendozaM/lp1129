    private void tbListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListaMouseClicked
        // TODO add your handling code here:
        //variables
        int fila = -1;
        int id_prov = 0;
        int id_depa = 0;
        String nombre = "";

        //entrada
        fila = tbLista.getSelectedRow();

        //proceso y salida
        if (fila>=0){
            id_prov = Integer.parseInt(tbLista.getValueAt(fila, 0)+"");
            nombre =tbLista.getValueAt(fila, 1)+"";
            id_depa = Integer.parseInt(tbLista.getValueAt(fila, 2)+"");
        }

        txtid.setText(id_prov+"");
        txtnombre.setText(nombre);
        logicaDepartamento.seleccionarItemCbo(cbodepartamentos, id_depa);
    }//GEN-LAST:event_tbListaMouseClicked

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        Provincia o = new Provincia(
                -1,
                txtnombre.getText(),
                //ID_DEPA(Integer o int)
                ((Departamento)cbodepartamentos.getSelectedItem()).getId_depa()
                );
        boolean rpta = logica.agregarProvincia(o);
        if(rpta){
            JOptionPane.showMessageDialog(this,"Se registro correctamente :) !!!");
        } else {
            JOptionPane.showMessageDialog(this,"No se pudo registrar :( !!!");
        }
        logica.imprimir(tbLista);
        
        limpiar();
        txtnombre.requestFocus();
    }//GEN-LAST:event_btnguardarActionPerformed


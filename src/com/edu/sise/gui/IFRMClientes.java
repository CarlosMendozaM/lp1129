    private void tbListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListaMouseClicked
        // TODO add your handling code here:
        //variables
        int fila = -1;
        int id_cli = 0;
        String nombre="",apellidos="";
        LocalDate fnacimiento;

        //entrada
        fila = tbLista.getSelectedRow();
        int x = 0;
        //proceso y salida
        if (fila>=0){
            id_cli = Integer.parseInt(tbLista.getValueAt(fila, x++)+"");
            nombre =tbLista.getValueAt(fila, x++)+"";
            apellidos =tbLista.getValueAt(fila, x++)+"";
            fnacimiento = LocalDate.parse(tbLista.getValueAt(fila, x++)+"");
            
            txtid.setText(id_cli+"");
            txtnombre.setText(nombre);
            txtapellidos.setText(apellidos);
            dtpfnac.getModel().setDate(fnacimiento.getYear(), 
            fnacimiento.getMonthValue()-1, fnacimiento.getDayOfMonth());
            dtpfnac.getModel().setSelected(true);
        }        

    }//GEN-LAST:event_tbListaMouseClicked

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        //Variables para la fecha
        int anio,mes,dia;
        anio = dtpfnac.getModel().getYear();
        mes = dtpfnac.getModel().getMonth()+1;
        dia = dtpfnac.getModel().getDay();
        
        Clientes o = new Clientes(
                -1,
                txtnombre.getText(),
                txtapellidos.getText(),
                LocalDate.of(anio, mes, dia));
        
        boolean rpta = logica.agregarClientes(o);
        if(rpta){
            JOptionPane.showMessageDialog(this,"Se registro correctamente :) !!!");
        } else {
            JOptionPane.showMessageDialog(this,"No se pudo registrar :( !!!");
        }
        logica.imprimir(tbLista);
        
        limpiar();
        txtnombre.requestFocus();
    }//GEN-LAST:event_btnguardarActionPerformed

